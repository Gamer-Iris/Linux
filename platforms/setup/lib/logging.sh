set -euo pipefail

# setup.sh 系処理のログ出力を共通化する。LOG_FILE は呼び出し元で定義済みであること。

######################################################################################################################################################
# log 関数
# @param  $1 : ログメッセージ
# @return なし
# @note   標準出力と LOG_FILE の両方へ同じ内容を出力する。
######################################################################################################################################################
function log() {
  local msg
  msg="[$(date '+%Y/%m/%d %T')] $1"
  echo -e "${msg}" | tee -a "${LOG_FILE}"
}

######################################################################################################################################################
# log_error 関数
# @param  $1 : エラーメッセージ
# @return なし
# @note   標準エラーと LOG_FILE の両方へ同じ内容を出力する。
######################################################################################################################################################
function log_error() {
  local msg
  msg="[$(date '+%Y/%m/%d %T')] [ERROR] $1"
  echo -e "${msg}" | tee -a "${LOG_FILE}" >&2
}
