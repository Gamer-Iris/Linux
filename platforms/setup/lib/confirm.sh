#!/bin/bash

######################################################################################################################################################
# ファイル   : confirm.sh
# 引数       : なし（setup.sh から source）
# 復帰値     : 0 （正常終了）
#            : 1 （異常終了）
#
#-----------------------------------------------------------------------------------------------------------------------------------------------------
# 【修正履歴】
# V-001      : 2026/06/27                 Gamer-Iris   新規作成
#
######################################################################################################################################################

set -euo pipefail

# setup.sh の本実行前確認を担当する。変更処理は行わず、実行可否のみ判定する。

######################################################################################################################################################
# confirm_execution 関数
# @param  なし
# @return なし
# @error  非対話実行で --yes 未指定、または確認入力が y 以外の場合は exit 1
# @note   dry-run / precheck は変更を伴わないため確認を省略する。
######################################################################################################################################################
function confirm_execution() {
  if [[ ${DRY_RUN} -eq 1 || ${PRECHECK} -eq 1 ]]; then
    return
  fi

  case "${MODE}" in
    all|control-plane|workers)
      log "注意: MODE=${MODE} はリモートノードのパッケージ変更、再起動、kubeadm init/join、アプリ適用を行う可能性があります。"
      if [[ ${ASSUME_YES} -eq 1 ]]; then
        log "--yes 指定により確認プロンプトを省略します"
        return
      fi
      if [[ ! -t 0 ]]; then
        log_error "非対話実行では --yes が必要です。事前に ./setup.sh --precheck と ./setup.sh all --dry-run を成功させてください。"
        exit 1
      fi
      read -r -p "本当に MODE=${MODE} を実行しますか？ 実行する場合は 'y' と入力してください: " answer
      if [[ "${answer}" != "y" ]]; then
        log_error "実行を中止しました。"
        exit 1
      fi
      ;;
  esac
}
